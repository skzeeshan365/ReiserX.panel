package com.reiserx.myapplication24.Models;

public class Two_object {
    public String path, type;

    public Two_object() {
    }

    public Two_object(String path, String type) {
        this.path = path;
        this.type = type;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
